# Sort using Python sort
x = [4, 1, 3, 2]
x.sort()
print('1) ', x, '\n')

# Sort using lexicographic order
x = ['Hello', 'Bob', 'How', 'are', 'you']
x.sort()
print('2) ', x, '\n')

# in operator to search a list
x = [1, 2, 3, 4]
found = 3 in x
print('3) ', found, '\n')
found = 5 in x
print('4) ', found, '\n')

# List construction
x = [1]*10
print('5) ', x, '\n')
x = list(range(10))
print('6) ', x, '\n')

# Find the minimum and maximum values
print('7) ', min(x), '\n')
print('8) ', max(x), '\n')








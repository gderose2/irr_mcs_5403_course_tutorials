# List search with index - index argument must be in the list!
x = [4, 3, 7, -5, 1]
idx = x.index(-5)
print('1) ', idx, '\n')

# Sort using lexicographic order
x = [1, 2, 2, 1, 5, 2, 5]
num_twos = x.count(2)
num_threes = x.count(3)
print('2) Number of twos = ', num_twos)
print('3) Number of threes = ', num_threes, '\n')

# Reversing a list
x = [1, 2, 3, 4]
print('4) ', x )
x.reverse()
print('5) ', x )






